#Type Casting in Python

print("Cast1: ", int(2.4), type(int(2.4)))
print("Cast2: ", str(5.4), type(str(5.4)))
print("Cast3: ", float(3), type(float(3)))

#unicode to character
print("Cast4: ", chr(97), type(chr(97)))

# character to unicode
print("Cast5: ", ord("a"), type(ord("a")))   


